package com.sda.fundamentals.loops;

public class DemoForLoops {

    public static void main(String[] args) {
        // when you know the number of loops

        // initial counter ; final condition ; step
        // from ; to ; step
        for (int i = 0; i < 5; i++) {
            System.out.println("i = " + i);
        }
    }
}
